#strings
first_name = "Kevy"
food = "Ugali"
print(first_name)
print(f"Hello {first_name}") #format specifier
print(f"You like {food}")

#integers
age = 25
print(f"You are {age} years old")

#float
price = 10.99
print(f"The price is ${price}")

#boolean
is_student = True
if is_student:
    print("You are a student")
else:
    print("You are not a student")